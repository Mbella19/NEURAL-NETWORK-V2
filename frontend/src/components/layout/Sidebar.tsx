export function Sidebar() {
  const pathname = usePathname();
  const connected = useConnected();
  const system = useSystem();

  const getPhaseLabel = () => {
    switch (system?.phase) {
      case 'analyst_training':
        return 'Analyst';
      case 'agent_training':
        return 'Agent';
      case 'backtest':
        return 'Backtest';
      default:
        return 'Idle';
    }
  };

  return (
    <aside className="sidebar flex flex-col h-screen">
      {/* Logo/Brand */}
      <div className="px-4 py-4 border-b border-zinc-800">
        <div className="flex items-center gap-2">
          <svg className="w-5 h-5 text-blue-500" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 10V3L4 14h7v7l9-11h-7z" />
          </svg>
          <span className="font-semibold text-sm text-zinc-100">HYBRID TRADER</span>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 py-4">
        {navItems.map((item) => {
          const isActive = pathname === item.href;
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                'sidebar-link',
                isActive && 'active'
              )}
            >
              {item.icon}
              <span>{item.label}</span>
            </Link>
          );
        })}
      </nav>

      {/* Status Footer */}
      <div className="px-4 py-4 border-t border-zinc-800">
        <div className="flex items-center gap-2 mb-2">
          <div className={cn('status-dot', connected ? 'connected' : 'disconnected')} />
          <span className="text-xs text-zinc-400">
            {connected ? 'Connected' : 'Disconnected'}
          </span>
        </div>
        <div className="text-xs text-zinc-500">
          Phase: <span className="text-zinc-300">{getPhaseLabel()}</span>
        </div>
        {system?.device && (
          <div className="text-xs text-zinc-500">
            Device: <span className="text-zinc-300 uppercase">{system.device}</span>
          </div>
        )}
      </div>
    </aside>
  );
}
